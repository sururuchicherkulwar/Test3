package Castingpractice;

public class Implicit {
public static void main(String[] args) {
// Implicit casting
	byte b = 20;              // 1 bytes
	System.out.println(b);
	short s = b;              //  2 byte
	System.out.println(b);
    int i =s;                 // 4 byte
    System.out.println(s);
    float f= i;                 // 4 byte
    System.out.println(f);
    long l= b;                   // 8 byte
    System.out.println(l);
	double d =l;                 // 8 byte
	System.out.println(d);
	
	
	

	
	
	
	
	
	
}
}
